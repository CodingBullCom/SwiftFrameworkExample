import Foundation

public class CBMath{
    
    public init(){
        
    }
    
    public func add(a: Int, b: Int) -> Int {
        return a+b
    }
    
    public func subtract(a: Int, b: Int) -> Int {
        return a-b
    }
    
    public func multiply(a: Int, b: Int) -> Int {
        return a*b
    }
    
    public func divide(a: Int, b: Int) -> Int {
        return a%b
    }
}
